                <h3><strong>VIA 2014 (International Arts Window) <br>
                “Performing arts from Ibero-America to the world”</strong></h3>
                <hr>
                <p>
                   The Ibero-American Theater Festival of Bogotá 2014 will hold the VII edition of its International Arts Window –VIA- (For its acronym in Spanish) from April 12 to April 15, at the facilities of the Chamber of Commerce of Bogotá. For this current version it counts again with the structure and Cultural Market platforms of Redlat/Circulart.
                  <br>
                      <br>
                </p>
                   
                <p>
                    VIA, is looking to consolidate an exporting platform for the Latin American performing arts, which, by the systematization of previous experiences and its integration to a perspective for the generation of a corporate culture for the arts, allows the international projection of the artistic production of this geographic region.
                  <br>
                      <br>
                </p>

                <p>
                  VIA is a favorable space for cultural, national and international businessmen to know more about the best proposals in dance, circus arts and theater produced in Latin America and to program them in festivals, artistic tours, specialized rooms and cultural fairs all over the world.</p>
              <p>&nbsp;</p>
                <p>
                </p>

                <p>
                   VIA is the opportunity to expand the distribution and consumption channels of cultural products, to update in the new alternatives of the world of cultural management and above all, to open the doors to the worldwide cultural diversity.
                     <br>
                     <br>
                </p>

                <p>
                    Producers, artistic agents or managers, creators, specialists in cultural marketing and artists will find in VIA the ideal opportunity for share business experiences, expand the reception and consumption channels of their cultural products and find the new alternatives of cultural management with lectures and workshops and, above all, to open the doors of the world to Ibero-American cultural diversity.
                  <br>
                      <br>
                </p>
                <p>
                   During the six previous editions of VIA, over 2060 theater managers from more than 40 countries have known, first hand, the work of Colombian and Ibero-American artists, who, through productive agreements were able to cross boundaries and reach international audiences.
                  <br>
                </p>
              <p>&nbsp; </p>
                <p>
                 The main components this year will be:
                  <br>
                  Business meetings.<br>
                  Specialized artistic showcases.<br>
Academic agenda.<br>
<br>
              </p>
              <p>
              Informal spaces for exchanging experiences and information that allows the attendants to know everything about the current state of the Ibero-American performing arts.              </p>
              <p>&nbsp;</p>
              <p>So, be part of this International Arts Window in the XIII FITB (International Theater Festival of Bogota) and sign up in the website <a href="http://www.festivaldeteatro.com.co">www.festivaldeteatro.com.co</a> There is daily follow up information about VIA 2012 (?) through our social networks:&nbsp;</p>
              <p><br>
                <a href="https://www.facebook.com/FestivalTeatroBogota" target="_blank">https://www.facebook.com/FestivalTeatroBogota</a>&nbsp;<br>
